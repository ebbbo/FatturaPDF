/** "TD01" → "TD01 - Invoice" (code + translated description) */
export function codeLabel(dict, code, lang) {
  if (!code) return "";
  const key = String(code).trim();
  const entry = dict[key] || dict[key.toUpperCase()];
  return entry ? `${key} - ${entry[lang]}` : key;
}
